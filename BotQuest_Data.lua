-- Helper: devuelve el índice de pool para un nivel dado
function BotQuestData:GetPoolIndex(level)
    if     level <= 10 then return 1
    elseif level <= 20 then return 2
    elseif level <= 30 then return 3
    elseif level <= 40 then return 4
    elseif level <= 50 then return 5
    elseif level <= 60 then return 6
    elseif level <= 70 then return 7
    else                    return 8
    end
end

