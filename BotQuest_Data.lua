-- ============================================================
--  Filtrar misiones por nivel del jugador
-- ============================================================
function BotQuestData:GetQuestsForLevel(level)
    local result = {}
    for _, quest in ipairs(self.QUESTS) do
        local minOk = level >= quest.minLevel
        local maxOk = (quest.maxLevel == nil) or (level <= quest.maxLevel)
        if minOk and maxOk then
            result[#result + 1] = quest
        end
    end
    return result
end

-- Formato legible de duración
function BotQuestData:FormatDuration(seconds)
    if seconds < 60 then
        return seconds .. "s"
    elseif seconds < 3600 then
        return math.floor(seconds / 60) .. " min"
    else
        return math.floor(seconds / 3600) .. "h " .. math.floor((seconds % 3600) / 60) .. "min"
    end
end

-- Formato legible de oro
function BotQuestData:FormatGold(copper)
    local gold   = math.floor(copper / 10000)
    local silver = math.floor((copper % 10000) / 100)
    local cop    = copper % 100
    local str = ""
    if gold   > 0 then str = str .. "|cffffd700" .. gold   .. "o|r " end
    if silver > 0 then str = str .. "|cffc0c0c0" .. silver .. "p|r " end
    if cop    > 0 then str = str .. "|cffb87333" .. cop    .. "c|r" end
    return str ~= "" and str or "0c"
end
